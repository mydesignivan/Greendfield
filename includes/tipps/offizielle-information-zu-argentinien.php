<p>
    Pass/Visa-Informationen <br />
    Für die Einreise nach Argentinien wird ein Reisepass benötigt. Für einen touristischen Aufenthalt von bis zu 90 Tagen ist kein Visum notwendig. Ein Visum muss beantragt werden, wenn der Aufenthalt 90 Tage überschreitet oder die Person zum Arbeiten, Studium oder Schulbesuch einreist. <br />
    Informationen zu den genauen Visabestimmungen können Sie bei den Konsulaten (siehe oben) erfahren.
</p>

