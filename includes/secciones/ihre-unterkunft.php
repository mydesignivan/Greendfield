<h3>Ihre Unterkunft</h3>

<p>Grundsätzlich gilt, dass alle Kurse mit Unterkunft angeboten werden. Für das Voluntariat bzw. das Praktikum kann dies bei entsprechender Nähe ebenfalls gelten. In diesem Zusammenhang handelt es sich in erster Linie um über lange Jahre bewährte Partner, die hoch motiviert sind und Ihnen einen vielversprechenden und angenehmen Familienanschluss gewähren, Ihnen aber auch gleichzeitig Ihre Privatsphäre einräumen. In Gastfamilien ist Frühstück inbegriffen. Halbpension ist möglich.</p>
<p>Auf Wunsch bieten wir aber auch Unterkunft ohne Familienanschluss und für Selbstverpfleger in einem Apartment. Noch vor Ihrer Anreise sprechen wir mit Ihnen die ausgesuchte Unterkunftsmöglichkeit durch und Sie kennen bereits „wo und mit wem“ Sie leben werden.</p>

<p>
    Kategorie 1:<br />
    Unterkunft in einer Gastfamilie mit eigenem Zimmer, Bad- und Küchenmitbenutzung und Frühstück (Sie sind für die Reinigkeit des Zimmers verantwortlich); Halbpension auf Wunsch.
</p>

<p>
    Kategorie 2:<br />
    Unterkunft in einer Gastfamilie, eigenem Zimmer, eigenem Bad, TV, Küchenmitbenutzung und Frühstück (Sie sind für die Reinigung des Zimmers verantwortlich); Halbpension auf Wunsch.
</p>

<p>
    Kategorie 3:<br />
    Unterkunft in einer Gastfamilie, eigenem Zimmer, TV, eigenem Bad, Küchenmitbenutzung und Frühstück, (Reinigung übernimmt die Gastfamilie); Halbpension auf Wunsch.
</p>

<p>
    Kategorie 4:<br />
    Unterkunft in einem Apartment mit Bad, Küche, TV (Reinigung überehmen Sie); Sie sind Eigenversorger.
</p>


<p>
    Kategorie 5:<br />
    Unterkunft in einem Apartment mit Bad, Küche, TV (Reinigung übernimmt Vermieter); Sie sind Eigenversorger.
</p>

<p>
    Kategorie 6:<br />
    Unterkunft in einem Hotel mit Bad, TV, Zimmerservice und Frühstück.
</p>

Unterkünfte können auch ohne einer Teilnahme am Programm Green Fields gebucht werden.

